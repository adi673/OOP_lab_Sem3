package Lab1;



class odd {
  
    public static void main(String[] args) {
        // Loop from 1 to 100
        for (int i = 1; i <= 100; i++) {
            // Check if the number is odd
            if (i % 2 != 0) {
                System.out.println(i); // Print the odd number
            }
        }
    
  }

}




